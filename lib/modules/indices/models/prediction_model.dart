  double biceps;
  double antebrazo;
  double muslo;
  double pantorrilla;
  double cuello;
  double pesoMagro;
  double pesoTotal;
  double porcientoGrasa;
  double pesoGrasa;

  PredictionModel(
      this.pecho,
      this.biceps,
      this.antebrazo,
      this.cuello,
      this.muslo,
      this.pantorrilla,
      this.pesoMagro,
      this.pesoTotal,
      this.porcientoGrasa,
      this.pesoGrasa) {
    pecho = pecho;
    biceps = biceps;
    antebrazo = antebrazo;
    muslo = muslo;
    pantorrilla = pantorrilla;
    cuello = cuello;
    pesoMagro = pesoMagro;
    pesoTotal = pesoTotal;
    porcientoGrasa = porcientoGrasa;
    pesoGrasa = pesoGrasa;
  }
}
