class PredictionModel {
  double pecho;
  double biceps;
  double antebrazo;
  double muslo;
  double pantorrilla;
  double cuello;
  double pesoMagro;

  PredictionModel(this.pecho, this.biceps, this.antebrazo, this.cuello,
      this.muslo, this.pantorrilla, this.pesoMagro) {
    pecho = pecho;
    biceps = biceps;
    antebrazo = antebrazo;
    muslo = muslo;
    pantorrilla = pantorrilla;
    cuello = cuello;
    pesoMagro = pesoMagro;
  }
}
