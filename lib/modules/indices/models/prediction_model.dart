class PredictionModel {
  double pecho;
  double biceps;
  double antebrazo;
  double muslo;
  double pantorrilla;
  double cuello;
  double pesoMagro;
  double pesoTotal;
  double porcientoGrasa;
  double pesoGrasa;
  double? cintura;
  double? cadera;

  PredictionModel(
      this.pecho,
      this.biceps,
      this.antebrazo,
      this.cuello,
      this.muslo,
      this.pantorrilla,
      this.pesoMagro,
      this.pesoTotal,
      this.porcientoGrasa,
      this.pesoGrasa,
      this.cintura,
      this.cadera) {
    pecho = pecho;
    biceps = biceps;
    antebrazo = antebrazo;
    muslo = muslo;
    pantorrilla = pantorrilla;
    cuello = cuello;
    pesoMagro = pesoMagro;
    pesoTotal = pesoTotal;
    porcientoGrasa = porcientoGrasa;
    pesoGrasa = pesoGrasa;
    cintura = cintura;
    cadera = cadera;
  }
}
